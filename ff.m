function [f,k,T1,T2] = ff(x)
    k = x(1); 
    T1 = x(2);
    T2 = x(3);
    A_c = [0 377 0 0 0 0; -0.0587 0 -0.1303 0 0 0; -0.0899 0 -0.1956 0.1289 0 0; 95.605 0 -816.0862 -20 0 1000; -0.0587 0 -0.1303 0 -1/3 0; -0.0587*k*T1/T2 0 -0.1303*k*T1/T2 0 (k/T2-k*T1/(3*T2)) -1/T2];
    f = min(-real(eig(A_c))./abs(eig(A_c)));
end
